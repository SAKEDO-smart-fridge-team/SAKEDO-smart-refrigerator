class I18nManager {
  constructor() {
    this.currentLang = localStorage.getItem("sakedo_lang") || "vi";
  }

  setLanguage(lang) {
    this.currentLang = lang;
    localStorage.setItem("sakedo_lang", lang);
    this.translatePage();
  }

  getLanguage() {
    return this.currentLang;
  }

  translate(key) {
    return i18nData[this.currentLang][key] || key;
  }

  translatePage() {
    const elements = document.querySelectorAll("[data-i18n]");
    elements.forEach(el => {
      const key = el.getAttribute("data-i18n");
      const translation = this.translate(key);

      if (el.tagName === "INPUT" && el.hasAttribute("placeholder")) {
        el.setAttribute("placeholder", translation);
      } else {
        // If there's an icon, we want to preserve it or replace only the text
        const icon = el.querySelector("i");
        if (icon) {
          // Keep the icon and update only the text
          // This assumes the text is a direct child or in a span
          const span = el.querySelector("span");
          if (span) {
            span.textContent = translation;
          } else {
            // Find text node and replace it
            Array.from(el.childNodes).forEach(node => {
              if (node.nodeType === Node.TEXT_NODE && node.textContent.trim().length > 0) {
                node.textContent = translation;
              }
            });
          }
        } else {
          el.textContent = translation;
        }
      }
    });
  }
}

window.sakedoI18n = new I18nManager();
